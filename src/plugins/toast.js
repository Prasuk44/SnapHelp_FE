import { toast, Bounce } from 'react-toastify'
import 'react-toastify/dist/ReactToastify.css';
export function successToast(message){
    toast.success(message, {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: false,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "colored",
        transition: Bounce,
        });
};

